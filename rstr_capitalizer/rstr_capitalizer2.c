#include <unistd.h>

int main( int ac, char **av)
{
    int i =0;
    int j = 1;
    
    if (ac == 1)
        write(1, "\n", 1);
    else
    {
        j = 1;
        while (j < ac)
        {
            while(av[1][i])
            {
                if(av[1][i] >= 'A' && av[1][i] <= 'Z')
                    av[1][i] += 32;
                if ((av[1][i] >= 'a' && av[1][i] <= 'z') && (av[1][i + 1] == ' ' \
                            || av[1][i + 1] == '\t' || av[1][i + 1] == '\0'))
                    av[1][i] -= 32;
                write(1, &av[1][i], 1);
                i++;
            }
            j++;
        }
    }
}